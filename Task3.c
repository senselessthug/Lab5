#include <stdio.h>

// Функція, яка виводить число у вигляді слів українською мовою
void print_number_in_words(int num) {
    // Масиви, що містять слова для сотень, десятків, 10-19 чисел і одиниць
    char *hundreds[] = {"", "сто", "двісті", "триста", "чотириста", "п'ятсот", "шістсот", "сімсот", "вісімсот", "дев'ятсот"};
    char *tens[] = {"", "", "двадцять", "тридцять", "сорок", "п'ятдесят", "шістдесят", "сімдесят", "ісімдесят", "дев'яносто"};
    char *teens[] = {"десять", "одинадцять", "дванадцять", "тринадцять", "чотирнадцять", "п'ятнадцять", "шістнадцять", "сімнадцять", "вісімнадцять", "дев'ятнадцять"};
    char *ones[] = {"", "один", "два", "три", "чотири", "п'ять", "шість", "сім", "вісім", "дев'ять"};

    // Розбиття числа на сотні, десятки і одиниці
    int h = num / 100;         // h - кількість сотень
    int t = (num % 100) / 10;  // t - кількість десятків 
    int o = num % 10;          // o - кількість одиниць 

    // Виведення відповідного слова для сотень
    printf("%s ", hundreds[h]);

    // Якщо десятки складають число від 10 до 19
    if (t == 1) {
        // Використовується масив "teens", щоб вивести правильне слово
        printf("%s\n", teens[o]);
    } else {
        // Якщо десятки не складають числа від 10 до 19
        // Виводиться відповідне слово для десятків і одиниць
        printf("%s %s\n", tens[t], ones[o]);
    }
}

int main() {
    int num;

    // Запит на введення тризначного числа
    printf("Введіть тризначне число: ");
    scanf("%d", &num);

    // Перевірка, чи число є тризначним
    if (num < 100 || num > 999) {
        // Якщо число не тризначне, виводиться повідомлення про помилку
        printf("Будь ласка, введіть тризначне число.\n");
    } else {
        // Якщо число тризначне, викликається функція для його виведення словами
        print_number_in_words(num);
    }

    return 0;
}
